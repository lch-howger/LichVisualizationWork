package model;

public class Rate {
    private String id;
    private String type;
    private String place;
    private String time;
    private String rate;

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getPlace() {
        return place;
    }

    public String getTime() {
        return time;
    }

    public String getRate() {
        return rate;
    }
}
